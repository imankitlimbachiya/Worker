package com.worker.app.model;

public class NotificationModel {

    String NotificationID, Title, TitleArabic, Notification, NotificationArabic, Type,
            NotificationDate, NotificationTime, Issue;

    public String getNotificationID() {
        return NotificationID;
    }

    public void setNotificationID(String notificationID) {
        NotificationID = notificationID;
    }

    public String getTitle() {
        return Title;
    }

    public void setTitle(String title) {
        Title = title;
    }

    public String getTitleArabic() {
        return TitleArabic;
    }

    public void setTitleArabic(String titleArabic) {
        TitleArabic = titleArabic;
    }

    public String getNotification() {
        return Notification;
    }

    public void setNotification(String notification) {
        Notification = notification;
    }

    public String getNotificationArabic() {
        return NotificationArabic;
    }

    public void setNotificationArabic(String notificationArabic) {
        NotificationArabic = notificationArabic;
    }

    public String getType() {
        return Type;
    }

    public void setType(String type) {
        Type = type;
    }

    public String getNotificationDate() {
        return NotificationDate;
    }

    public void setNotificationDate(String notificationDate) {
        NotificationDate = notificationDate;
    }

    public String getIssue() {
        return Issue;
    }

    public void setIssue(String issue) {
        Issue = issue;
    }

    public String getNotificationTime() {
        return NotificationTime;
    }

    public void setNotificationTime(String notificationTime) {
        NotificationTime = notificationTime;
    }
}
