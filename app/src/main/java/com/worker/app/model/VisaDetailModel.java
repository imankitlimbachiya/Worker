package com.worker.app.model;

public class VisaDetailModel {

    String CountryID, CountryName, WorkersCount, Visa_Identity, CountryNameArabic, VisaType;

    public String getCountryID() {
        return CountryID;
    }

    public void setCountryID(String countryID) {
        CountryID = countryID;
    }

    public String getCountryName() {
        return CountryName;
    }

    public void setCountryName(String countryName) {
        CountryName = countryName;
    }

    public String getWorkersCount() {
        return WorkersCount;
    }

    public void setWorkersCount(String workersCount) {
        WorkersCount = workersCount;
    }

    public String getVisa_Identity() {
        return Visa_Identity;
    }

    public void setVisa_Identity(String visa_Identity) {
        Visa_Identity = visa_Identity;
    }

    public String getCountryNameArabic() {
        return CountryNameArabic;
    }

    public void setCountryNameArabic(String countryNameArabic) {
        CountryNameArabic = countryNameArabic;
    }

    public String getVisaType() {
        return VisaType;
    }

    public void setVisaType(String visaType) {
        VisaType = visaType;
    }
}
